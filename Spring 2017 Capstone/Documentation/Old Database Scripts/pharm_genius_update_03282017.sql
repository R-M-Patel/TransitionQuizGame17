  IF new.correct_flag = 'Y' THEN
    UPDATE question
    SET
      times_correctly_answered = times_correctly_answered + 1,
      times_answered = times_answered + 1
    WHERE question_id = new.question_id;
    UPDATE user
    SET score = score + new.score
    WHERE username = new.username;
  ELSE
  	UPDATE question
    SET
      times_answered = times_answered + 1
    WHERE question_id = new.question_id;
  END IF;
END
$$
